package adapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.List;

import activity.AudioBookDetailsFragment;
import activity.AudioPlayFragment;

/**
 * Created by bikram on 1/13/16.
 */
public class PagerAdapter extends FragmentPagerAdapter {
    int mNumOfTabs;

    public PagerAdapter(FragmentManager fm, int NumOfTabs) {
        super(fm);
        this.mNumOfTabs = NumOfTabs;
    }


    @Override
    public Fragment getItem(int position) {
        switch (position) {
            case 0:
                AudioPlayFragment tab1 = new AudioPlayFragment();
                return tab1;
            case 1:
                AudioBookDetailsFragment tab2 = new AudioBookDetailsFragment();
                return tab2;

            default:
                return null;
        }
    }

    @Override
    public int getCount() {
        return mNumOfTabs;
    }
}
